package chap02;

import java.util.Scanner;

// 입력 받은 10진수를 2진수 ~ 36진수로 기수 변환하여 나타냄
public class CardConvRev {

	private static int cardConvR(int x, int r, char[] d) {
		// 정숫값 x를 r진수로 변환하여 배열 d에 아랫자리부터 넣어두고 자릿수를 반환합니다.
		int digits = 0;						 	// 변환 후의 자릿 수
		String dchar = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
		
		do {
			d[digits++] = dchar.charAt(x % r); 	// r로 나눈 나머지를 저장
			x /= r;
		} while(x != 0);
		return digits;
	}
	
	private static int cardConv(int x, int r, char[] d) {
		int digits = 0;
		String dchar = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
		
		do {
			d[digits++] = dchar.charAt(x % r); 	// r로 나눈 나머지를 저장
			x /= r;
		} while(x != 0);
		
		for(int i=0; i<digits/2; i++) {
			char temp = d[i];
			d[i] = d[digits-i-1];
			d[digits-i-1] = temp;
		}
		return digits;
		
	}
	
	private static int cardConvProcess(int x, int r, char[] d) {
		// 정숫값 x를 r진수로 변환하여 배열 d에 아랫자리부터 넣어두고 자릿수를 반환합니다.
		int digits = 0;						 	// 변환 후의 자릿 수
		String dchar = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
		
		System.out.printf(r + "|%3d\n",x);
		System.out.print("+-----\n");
		
		do {
			d[digits++] = dchar.charAt(x % r); 	// r로 나눈 나머지를 저장
			if(x/r == 0)
				System.out.printf(" %3d ... %d\n", x/r, x%r);
			else {
				System.out.printf(r + "|%3d ... %d\n", x/r, x%r);
				System.out.print("+-----\n");
			}
			x /= r;
		} while(x != 0);
		return digits;
	}
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int no;						// 변환하는 정수
		int cd;						// 기수
		int dno;					// 변환 후의 자릿수
		int retry;					// retry
		char[] cno = new char[32];	// 변환 후 각 자리의 숫자를 넣어두는 문자의 배열
		
		System.out.println("10진수를 기수 변환합니다.");
		do {
			do {
				System.out.print("변환하는 음이 아닌 정수 : ");
				no = sc.nextInt();
			} while(no < 0);
			
			do {
				System.out.print("어떤 진수로 변환할까요 ? (2~36) : ");
				cd = sc.nextInt();
			} while(cd<2 || cd>36);
			
			dno = cardConvR(no, cd, cno);		// no를 cd진수로 변환.
			
			System.out.print(cd + "진수로는 ");
			for(int i = dno - 1; i>=0; i--)		// 윗자리부터 차례로 나타냄.
				System.out.print(cno[i]);
			System.out.println("입니다.");
			
			System.out.print("한번 더 할까요? (1.예/0.아니오) : ");
			retry = sc.nextInt();
		} while (retry == 1);
		System.out.println();
		
		
		// Q6. 배열의 앞쪽에 아랫자리가 아니라 윗자리를 넣어두는 메서드를 작성하라.
		// static int cardConv(int x, int r, char[] d)
		System.out.println("배열의 앞쪽에 윗자리를 넣어두는 메서드를 이용하여 10진수를 기수 변환합니다.");
		do {
			do {
				System.out.print("변환하는 음이 아닌 정수 : ");
				no = sc.nextInt();
			} while(no < 0);
			
			do {
				System.out.print("어떤 진수로 변환할까요 ? (2~36) : ");
				cd = sc.nextInt();
			} while(cd<2 || cd>36);
			
			dno = cardConv(no, cd, cno);		// no를 cd진수로 변환.
			
			System.out.print(cd + "진수로는 ");
			for(int i = dno - 1; i>=0; i--)		// 아랫자리부터 차례로 나타냄.
				System.out.print(cno[i]);
			System.out.println("입니다.");
			
			System.out.print("한번 더 할까요? (1.예/0.아니오) : ");
			retry = sc.nextInt();
		} while (retry == 1);
		System.out.println();
		
		
		// Q7. 기수변환 과정을 자세히 나타내는 프로그램을 작성하라.
		System.out.println("10진수를 기수 변환합니다.");
			do {
				System.out.print("변환하는 음이 아닌 정수 : ");
				no = sc.nextInt();
			} while(no < 0);
			
			do {
				System.out.print("어떤 진수로 변환할까요 ? (2~36) : ");
				cd = sc.nextInt();
			} while(cd<2 || cd>36);
			
			dno = cardConvProcess(no, cd, cno);		// no를 cd진수로 변환.
			
			System.out.print(cd + "진수로는 ");
			for(int i = dno - 1; i>=0; i--)		// 윗자리부터 차례로 나타냄.
				System.out.print(cno[i]);
			System.out.println("입니다.");
	}
}
